    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
        $servicios = Servicio::paginate(10)->all();
        return view('servicios.create', compact('servicios'))
        ->with("servicios", $servicios);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $servicio = new Servicio();
        $servicio->idServicio = $request->idServicio;
        $servicio->nombreServicio = $request->nombreServicio;
        $servicio->save();
        return redirect()->route('servicios.create',$servicio)->with('info', 'El nuevo servicio se ha agregado en la base de datos satisfactoriamente');
    }

